# + : 더하기
# - : 빼기
# * : 곱하기
# / : 나누기

print(7 + 14)  # 21
print(7 - 14)  # -7 (계산 결과가 음수여도 출력 가능)
print(7 * 14)  # 98
print(7 / 14)  # 0.5

print(75 + 80 / 2)  # 115.0 (나누기 먼저 계산)
print((75 + 80) / 2)  # 77.5 괄호로 감싸서 계산 순서 지정

