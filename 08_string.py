print("=== 실습 - 설비 정보 출력 카드 만들기 ===")
# 목표 : 여러 문자열 숫자 정보를 모아 여러 줄 정보 카드로 출력
# 단계 :
# 코드(int) 상태 가동시간 점검일을 변수로 각자 선언
# -> 한 번의 출력으로 여러 줄의 문자열이 보이도록 구성
# 예상결과 : 설비/상태/가동/점검 4줄 카드 출력
# 설비: PUMP_A       > str
# 상태: 정상         > str
# 가동: 1200         > !! int !!
# 점검: 2026-07-16   > str
code = "PUMP_A"
state = "정상"
hours = 1200
day = "2026-07-16"
card = "설비: " + code + "\n상태" + state + "\n가동" + str(hours) + "시간\n점검: " + day
print(card)

